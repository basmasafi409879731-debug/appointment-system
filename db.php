<?php
// 1. إنشاء اتصال بقاعدة البيانات باستخدام الدالة mysqli_connect
// المعاملات: (اسم السيرفر، اسم المستخدم، كلمة المرور، اسم قاعدة البيانات)
$conn = mysqli_connect("localhost", "root", "", "appointment_system");

// 2. التحقق من نجاح عملية الاتصال
// في حال وجود خطأ، تقوم الدالة die بإيقاف تنفيذ الكود وطباعة رسالة الخطأ المحددة
if (!$conn) {
    // عرض رسالة توضح سبب فشل الاتصال باستخدام mysqli_connect_error
    die("فشل الاتصال بقاعدة البيانات: " . mysqli_connect_error());
}
?>